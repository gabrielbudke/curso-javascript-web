const a = 7
let b = 3

b += a // atribuição de adição ->  b = b + a
console.log(b)

b -= 4 // atribuição de subtração -> b = b - 4
console.log(b)

b *= 2 // atribuição multiplicação -> b = b * 2
console.log(b)

b /= 2 // atribuição multiplicao -> b = b / 2
console.log(b)

b %= 2 // atribuição modulo/resto da divisao
console.log(b)